CREATE TABLE JOB(
JOBID INT NOT NULL PRIMARY KEY,
JOBTITLE VARCHAR(20),
MINSALARY DECIMAL(10,2),
MAXSALARY DECIMAL(10,2)
);

CREATE TABLE LOGIN
(
LOGINID VARCHAR(20),
PASSWD VARCHAR(20),
BIRTH VARCHAR(20),
SEX INT DEFAULT 1,
MAIL VARCHAR(20),
TEL VARCHAR(20),
SALARY DECIMAL(10,2),
JOBID INT DEFAULT 0,
LEVEL INT  DEFAULT 1,
  PRIMARY KEY (LOGINID) ,
FOREIGN KEY(JOBID) REFERENCES JOB(JOBID)
);
